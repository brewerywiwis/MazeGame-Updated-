	public static void drawMap(GraphicsContext gc,int num,double ScreenWidth,double ScreenHeight) {
		gc.setFill(Color.BLACK);
		int[][] map = getMap(num);
		int RowColumn = map.length;
		for (int i = 0; i <= RowColumn; i++) {
			gc.fillRect(0,i * (ScreenWidth/RowColumn), ScreenWidth, 1);
		}
		for (int j = 0; j <= RowColumn; j++) {
			gc.fillRect(j * (ScreenHeight/RowColumn),0, 1, ScreenHeight);
		}
		gc.setFill(Color.RED);
		for (int i = 0; i < map.length; i++) {
			for (int j = 0; j < map[i].length; j++) {
				if (map[i][j] == 1) {
					gc.fillRect(j*ScreenWidth/RowColumn,
							i*ScreenHeight/RowColumn, ScreenWidth/RowColumn, ScreenHeight/RowColumn);
				}
			}
		}
	}
